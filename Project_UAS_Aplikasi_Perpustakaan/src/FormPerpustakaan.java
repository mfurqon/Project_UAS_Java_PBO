    private void cbgenreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbgenreActionPerformed
        // TODO add your handling code here:
        if(cbgenre.getSelectedItem().equals("PILIH")){
            //            cbjudul.setModel("PILIH");
            tpenulis.setText("");
            tpenerbit.setText("");
        }
        else if(cbgenre.getSelectedItem().equals("Fantasi")){
            String[] judulBukuFantasi = perpus.getJudulBuku("Fantasi");
            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(judulBukuFantasi);
            cbjudul.setModel(model);

        }
        else if(cbgenre.getSelectedItem().equals("Aksi")){
            String[] judulBukuAksi = perpus.getJudulBuku("Aksi");
            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(judulBukuAksi);
            cbjudul.setModel(model);
        }
        else if(cbgenre.getSelectedItem().equals("Edukasi")){
            String[] judulBukuEdukasi = perpus.getJudulBuku("Edukasi");
            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(judulBukuEdukasi);
            cbjudul.setModel(model);
        }
        else if(cbgenre.getSelectedItem().equals("Romansa")){
            String[] judulBukuRomansa = perpus.getJudulBuku("Romansa");
            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(judulBukuRomansa);
            cbjudul.setModel(model);
        }
    }//GEN-LAST:event_cbgenreActionPerformed

