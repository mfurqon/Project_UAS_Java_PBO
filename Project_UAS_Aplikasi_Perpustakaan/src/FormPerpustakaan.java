    private void bhitunghariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhitunghariActionPerformed
        // TODO add your handling code here:
        Date waktuPeminjaman = dcwaktu_peminjaman.getDate();
        int durasiPeminjaman = (int) sdurasiPeminjaman.getValue();

        //        int durasi = Integer.parseInt(durasiPeminjaman);

        Calendar calendar = Calendar.getInstance();
        calendar.setTime(waktuPeminjaman);
        calendar.add(Calendar.DAY_OF_MONTH, durasiPeminjaman);

        Date tanggalHasil = calendar.getTime();

        SimpleDateFormat dateFormat = new SimpleDateFormat("dd MMM yyyy");
        String tanggalHasilStr = dateFormat.format(tanggalHasil);

        twaktu_pengembalian.setText(tanggalHasilStr);
    }//GEN-LAST:event_bhitunghariActionPerformed

